public class Aritmatika{
    public static void main(String[] args){
        int a = 7;
        int b = 5;
        System.out.println("Tambah = " + (a+b));
        System.out.println("Kurang = " + (a-b));
        System.out.println("Kali = " + (a*b));
        System.out.println("Bagi = " + (a/b));
        System.out.println("Modular = " + (a%b));
        System.out.println("Increment a = " + (++a));
        System.out.println("Increment b = " + (++b));
        System.out.println("Decrement a = " + (--a));
        System.out.println("Decrement b = " + (--b));
        // System.out.println(a++);
        // System.out.println(a);
    }
}